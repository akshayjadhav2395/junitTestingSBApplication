package com.example.demo;

public class Calculator {


    public int doSum(int a, int b)
    {
        return a+b;
    }

    public int doMultiply(int a, int b)
    {
        return a*b;
    }

    public int doDivision(int a, int b)
    {
        return a/b;
    }

    public boolean compare(int a, int b)
    {
        return  a==b;
    }
}
